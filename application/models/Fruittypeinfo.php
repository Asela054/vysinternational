    public function Fruittypeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_fruit_type');
        $this->db->where('idtbl_fruit_type', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_fruit_type;
        $obj->type=$respond->row(0)->type;

        echo json_encode($obj);
    }
